def convertir_str_a_fecha(str_fecha):
	fecha = datetime.datetime.strptime(str_fecha,'%m/%d/%Y')
	if type(fecha) == datetime.date:
		return fecha
	else:
		date_fecha = fecha.date()
		return date_fecha




